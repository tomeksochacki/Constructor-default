package com.example.constructor;

public class Animals {
    private String name;
    private int age;
    private String type;

    public void setName(String name){
        this.name = name;
    }
    public String getName(){
        return name;
    }
    public void setAge(int age){
        this.age = age;
    }
    public int getAge(){
        return age;
    }
    public void setType(String type){
        this.type = type;
    }
}
